class Calculator:
   def multiply(self, x, y):
       """Функция умножения калькулятора"""
       return x * y

   def division(self, x, y):
       """Функция деления калькулятора"""
       return x / y

   def subtraction(self, x, y):
       """Функция вычитания калькулятора"""
       return x - y

   def adding(self, x, y):
       """Функция сложения калькулятора"""
       return x + y